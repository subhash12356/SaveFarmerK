package com.example.kalpesh.savefarmer.model;

public class Wholesellerdetails {

    private String Whole_seller_id;
    private String Whole_seller_Name;
    private String Address;
    private String State_Name;
    private String City_Name;
    private String Email_id;
    private String Contact_No;
    private String Pin_Code;

    public String getWhole_seller_id() {
        return Whole_seller_id;
    }

    public void setWhole_seller_id(String whole_seller_id) {
        Whole_seller_id = whole_seller_id;
    }

    public String getWhole_seller_Name() {
        return Whole_seller_Name;
    }

    public void setWhole_seller_Name(String whole_seller_Name) {
        Whole_seller_Name = whole_seller_Name;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getState_Name() {
        return State_Name;
    }

    public void setState_Name(String state_Name) {
        State_Name = state_Name;
    }

    public String getCity_Name() {
        return City_Name;
    }

    public void setCity_Name(String city_Name) {
        City_Name = city_Name;
    }

    public String getEmail_id() {
        return Email_id;
    }

    public void setEmail_id(String email_id) {
        Email_id = email_id;
    }

    public String getContact_No() {
        return Contact_No;
    }

    public void setContact_No(String contact_No) {
        Contact_No = contact_No;
    }

    public String getPin_Code() {
        return Pin_Code;
    }

    public void setPin_Code(String pin_Code) {
        Pin_Code = pin_Code;
    }
}
